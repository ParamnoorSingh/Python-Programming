# WAP to demonstrate type checking of various data types and demonstrate the use of following built in function
int_a = -5
float_a = 5.5
string_a = "hello123"
list_a = [1,2,3,4,5,6]
print(type(int_a))
print(type(float_a))
print(type(string_a))
print(type(list_a))
print("absolute value of the number: ",abs(int_a))
print("length of the string: ",len(string_a))
print("min value in the list: ",min(list_a))
print("round off value: ",round(float_a))
print("Is the string alphanumeric? ",string_a.isalnum())